using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TrabalhoAlg3
{
    public class Cursos
    {
        private string _nome;
        private int _carga;

        public string nome
        {
            get
            {
                return _nome;
            }
            set
            {
                _nome = nome;
            }
        }

        public int carga
        {
            get
            {
                return _carga;
            }
            set
            {
                _carga = carga;
            }
        }
    }

}
